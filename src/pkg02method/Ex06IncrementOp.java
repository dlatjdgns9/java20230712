package pkg02method;

public class Ex06IncrementOp {
    public static void main(String[] args) {
        int n =2;
        n++;
        System.out.println(n);
        ++n;
        System.out.println(n);
        System.out.println(n++);
        System.out.println(++n);
        System.out.println(n--);
        System.out.println(--n);
    }
}
