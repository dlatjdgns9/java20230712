package pkg02method;

public class Ex05BitOp {
    public static void main(String[] args) {
        int n=2;
        int result = n>>2;
        System.out.println(result);
        result = n<< 2 ;
        System.out.println(result);
        result = 2 | 3;
        System.out.println(result);
        result = 2^3;
        System.out.println(result);
    }
}
